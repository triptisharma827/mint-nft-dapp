// Define the MintNFT component
function MintNFT() {
    const [isMinting, setIsMinting] = useState(false); // State to track whether minting is in progress

    // Function to handle the minting of NFT
    const handleMint = async () => {
        if (!window.ethereum) {
            alert('Metamask is not installed. Please install Metamask.'); // Alert if Metamask is not installed
            return;
        }

        const web3 = new Web3(window.ethereum); // Create a Web3 instance using the injected Ethereum provider

        try {
            setIsMinting(true); // Set the minting flag to true

            const accounts = await web3.eth.requestAccounts(); // Get the connected Ethereum accounts
            const contractAddress = '0x6f71ef40b1647e96fabE462E269818280120e4cB'; // Replace with your actual contract address
            const contract = new web3.eth.Contract(ABI, contractAddress); // Create a contract instance using the ABI and contract address

            await contract.methods.mintNFT().send({ from: accounts[0], value: web3.utils.toWei('0.01', 'ether') }); // Call the mintNFT function on the contract
            alert('NFT Minted successfully!'); // Alert the user about successful minting
        } catch (error) {
            console.error(error); // Log any errors that occur
            alert('An error occurred while minting NFT.'); // Alert the user about the error
        } finally {
            setIsMinting(false); // Set the minting flag back to false
        }
    };

    return (
        <div>
            {/* Button to trigger the minting process */}
            <button onClick={handleMint} className='Btn' disabled={isMinting}>Mint NFT</button>
        </div>
    );
}

export default MintNFT; // Export the MintNFT component
